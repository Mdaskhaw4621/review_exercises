
public class PrimitiveTypeVars {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		//declare a int and assigns the value
		int year_oud = 25;
		
		//declare a float and, after, assigns a value
		float value;
		value = 1.99f;
		
		//declaring a boolean
		boolean TrueOrFalse = false;
		TrueOrFalse = true;
		
		//declaring a char
		char letterA = 'A';
		letterA = 65; //ASCII value for A
		letterA = '\u0041'; //Unicode value for A
		
		//declaring a byte
		byte b = 127;
		
		//declaring a short
		short s = 1024;
		
		//declaring a long
		long l = 1234567890;
		
		//declaring a double
		double d = 100.0;
		
		//declare multiple
		int var1 = 0, var2 = 1, var3 = 2, var4;
	}

}
